/**
 * @returns {import('unocss').Preset}
 */
function presetPico() {
  return {
    name: "unocss-preset-pico",
    extendTheme: (theme) => {
      /** @type { any} */ const newTheme = theme;
      if (newTheme.colors !== undefined) {
        newTheme.colors = { ...newTheme.colors, ...picoCssColors };
      } else {
        newTheme.colors = { ...picoCssColors };
      }

      return newTheme;
    },
  };
}

export default presetPico;
